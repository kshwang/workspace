package java11.st4;

public class Oper {
    
    public static int Add(int x, int y) {
        int sum = x + y;
        return sum;
    }
    
    public static int Minus(int x, int y) {
        int sum = x - y;
        return sum;
    }
    
    public static int Mul(int x, int y) {
        int sum = x * y;
        return sum;
    }

    public static double Div(int x, int y) {
        double sum = (double)x / (double)y;
        return sum;
    }
    
}
