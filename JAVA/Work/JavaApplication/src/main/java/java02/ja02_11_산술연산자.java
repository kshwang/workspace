package java02;
   
public class ja02_11_산술연산자 {
    
    public static void main(String[] args) {
    
        int x = 4;
        int y = 5;
        int res = 0;
        
        res = x +y;
        System.out.println(res);

        res = x - y;
        System.out.println(res);
        
        res = x * y;
        System.out.println(res);
    
        res = x / y;
        System.out.println(res);

        res = x % y;
        System.out.println(res);
    }
    
}
